<h3 class="h3">方針</h3>
<p class="p">当サイトは、サイバー攻撃や荒らしに関する情報を報道しないことを方針とします。</p>
<h3 class="h3">ガイドライン</h3>
<ul class="ul">
    <li class="li">サイバー攻撃や荒らしについての報道禁止: これらの問題に関する詳細や情報を報道しないことで、混乱や悪影響を避けます。</li>
</ul>
