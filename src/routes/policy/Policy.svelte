</ul>

<h2 class="h2">9. 誹謗中傷や差別的発言の禁止</h2>
<h3 class="h3">方針</h3>
<p class="p">
  当サイトは、誹謗中傷や差別的発言を断固として認めず、健全なコミュニケーション環境を維持します。
</p>
<h3 class="h3">ガイドライン</h3>
<ul class="ul">
  <li class="li">誹謗中傷の禁止: 誹謗中傷や個人攻撃を含む発言を禁止します。</li>
  <li class="li">
    差別的発言の禁止:
    人種、性別、宗教、国籍、性的指向などに基づく差別的発言を禁止します。
  </li>
  <li class="li">
    違反行為への対応:
    誹謗中傷や差別的発言が確認された場合は、適切な措置を講じ、場合によっては法的対応も検討します。
  </li>
</ul>

