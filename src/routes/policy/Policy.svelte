<h1 class="h1">運営方針兼ガイドライン</h1>
<p class="p">我々はこの運営方針とガイドラインを厳守して運営します。</p>

<h2 class="h2">1. 引用元の明確化と一次情報の重視</h2>
<h3 class="h3">方針</h3>
<p class="p">
  当サイトは、信頼性の高い情報を提供するために、引用元を明確にし、可能な限り一次情報に基づいた記事作成を行います。
</p>
<h3 class="h3">ガイドライン</h3>
<ul class="ul">
  <li class="li">
    引用元の明示:
    記事内で引用した情報源を明確に示し、読者が情報の出所を確認できるようにします。
  </li>
  <li class="li">
    一次情報の重視:
    公式発表、原資料、直接インタビューなど、可能な限り一次情報に基づいて記事を作成します。
  </li>
</ul>

<h2 class="h2">2. 中立的な視点と多様な意見の掲載</h2>
<h3 class="h3">方針</h3>
<p class="p">
  当サイトは、中立的な視点を保ちつつ、多様な意見を取り入れたバランスの取れた報道を目指します。
</p>
<h3 class="h3">ガイドライン</h3>
<ul class="ul">
  <li class="li">
    中立的な視点の保持:
    記事を書く際には中立的な視点を保ち、特定の立場に偏らないよう努めます。
  </li>
  <li class="li">
    多様な意見の掲載:
    関連する複数の意見や視点を取り上げ、バランスの取れた報道を行います。
  </li>
</ul>

<h2 class="h2">3. 個人情報の保護</h2>
<h3 class="h3">方針</h3>
<p class="p">
  当サイトは、個人情報の保護に最大限の注意を払い、倫理的な報道を行います。
</p>
<h3 class="h3">ガイドライン</h3>
<ul class="ul">
  <li class="li">
    個人情報の保護:
    記事に登場する個人のプライバシーを尊重し、必要な場合は匿名化を行います。
  </li>
  <li class="li">
    倫理的な報道:
    報道の倫理を遵守し、センセーショナルな表現や過度な誇張を避けます。
  </li>
</ul>

<h2 class="h2">4. 誤りの訂正と謝罪</h2>
<h3 class="h3">方針</h3>
<p class="p">
  当サイトは、誤報や不正確な情報があった場合、速やかに訂正し、必要に応じて謝罪します。
</p>
<h3 class="h3">ガイドライン</h3>
<ul class="ul">
  <li class="li">
    誤りの速やかな訂正:
    誤報や不正確な情報があった場合は速やかに訂正し、読者に謝罪します。
  </li>
  <li class="li">
    訂正の透明性: 訂正内容は読者に対して透明に公開し、信頼性を維持します。
  </li>
</ul>

<h2 class="h2">5. ライターへのガイドラインの共有と遵守</h2>
<h3 class="h3">方針</h3>
<p class="p">
  当サイトは、すべてのライターにガイドラインを共有し、理解と遵守を徹底します。
</p>
<h3 class="h3">ガイドライン</h3>
<ul class="ul">
  <li class="li">
    ガイドラインの共有:
    すべてのライターに対してこのガイドラインを共有し、理解と遵守を徹底します。
  </li>
  <li class="li">
    定期的な研修:
    ライター向けの定期的な研修を実施し、ガイドラインの実践を促進します。
  </li>
</ul>

